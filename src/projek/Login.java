    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
        try{
            java.sql.Connection conn = (java.sql.Connection) Koneksi.getKoneksi();
            java.sql.Statement stm = conn.createStatement();
            java.sql.ResultSet sql = stm.executeQuery("SELECT * FROM karyawan WHERE id_karyawan='"+ username.getText() +"' AND password='" + password.getText() + "'");
            if(sql.next()){
                if(sql.getString("hak_akses").equals("Admin")){
                //if(username.getText().equals(rs.getString("id_karyawan")) && password.getText().equals(rs.getString("password"))){
                    JOptionPane.showMessageDialog(null, "Anda masuk sebagai Admin");
                    Menu mn = new Menu();
                    mn.Admin();
                    mn.setVisible(true);
                    this.hide();
                }
                else if(sql.getString("hak_akses").equals("Kasir")){
                    JOptionPane.showMessageDialog(null, "Anda masuk sebagai Kasir");
                    Menu mn = new Menu();
                    mn.Kasir();
                    mn.setVisible(true);
                    this.hide();
                }
            }else{
                JOptionPane.showMessageDialog(null, "username password salah");
            }
        } catch (Exception e){
            JOptionPane.showMessageDialog(this, ""+ e.getMessage());
        }
    }//GEN-LAST:event_jButton1MouseClicked

